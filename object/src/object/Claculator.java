package object;

public class Claculator {
	
	public int claculator(int num1, int num2) {
		return num1 + num2;
	}
	
	public int minus(int num3, int num4) {
		return num3 - num4;
	}
	
	public int multipul(int num5, int num6) {
		return num5 * num6; 
	} 
	
	public int division(int num7, int num8) {
		return num7 / num8;
	}
	
	public int percent(int num9, int num10) {
		return num9 % num10;
	}

}
